import 'package:flutter/material.dart';

Color appBlackL(double opacity) => Color.fromRGBO(73, 73, 73, opacity);
Color appBlack(double opacity) => Color.fromRGBO(24, 24, 24, opacity);
Color appWhite(double opacity) => Color.fromRGBO(242, 242, 242, opacity);
Color appGrey(double opacity) => const Color.fromARGB(255, 116, 114, 114);
